package com.tecnotree.model;

public class Rule {

	private String id;
	private RuleDetail ruleDetail;
	
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public RuleDetail getRuleDetail() {
		return ruleDetail;
	}
	public void setRuleDetail(RuleDetail ruleDetail) {
		this.ruleDetail = ruleDetail;
	}
	
}
